package com.teach.entity;

public class Collectlist {
    private String collectlistId;

    private String userId;

    private String collectlistMusicIds;

    public String getCollectlistId() {
        return collectlistId;
    }

    public void setCollectlistId(String collectlistId) {
        this.collectlistId = collectlistId;
    }

    public String getUserId() {
        return userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }

    public String getCollectlistMusicIds() {
        return collectlistMusicIds;
    }

    public void setCollectlistMusicIds(String collectlistMusicIds) {
        this.collectlistMusicIds = collectlistMusicIds;
    }
}