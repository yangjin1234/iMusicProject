package com.teach.entity;

public class Anchor {
    private String anchorId;

    private String anchorUserId;

    private String anchorName;

    private String anchorState;

    public String getAnchorId() {
        return anchorId;
    }

    public void setAnchorId(String anchorId) {
        this.anchorId = anchorId;
    }

    public String getAnchorUserId() {
        return anchorUserId;
    }

    public void setAnchorUserId(String anchorUserId) {
        this.anchorUserId = anchorUserId;
    }

    public String getAnchorName() {
        return anchorName;
    }

    public void setAnchorName(String anchorName) {
        this.anchorName = anchorName;
    }

    public String getAnchorState() {
        return anchorState;
    }

    public void setAnchorState(String anchorState) {
        this.anchorState = anchorState;
    }
}