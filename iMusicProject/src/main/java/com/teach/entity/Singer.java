package com.teach.entity;

public class Singer {
    private String singerId;

    private String singerName;

    private Integer singerAge;

    private String singerSex;

    private String singerDescription;

    private String singerPicture;

    private String singerNationality;

    public String getSingerId() {
        return singerId;
    }

    public void setSingerId(String singerId) {
        this.singerId = singerId;
    }

    public String getSingerName() {
        return singerName;
    }

    public void setSingerName(String singerName) {
        this.singerName = singerName;
    }

    public Integer getSingerAge() {
        return singerAge;
    }

    public void setSingerAge(Integer singerAge) {
        this.singerAge = singerAge;
    }

    public String getSingerSex() {
        return singerSex;
    }

    public void setSingerSex(String singerSex) {
        this.singerSex = singerSex;
    }

    public String getSingerDescription() {
        return singerDescription;
    }

    public void setSingerDescription(String singerDescription) {
        this.singerDescription = singerDescription;
    }

    public String getSingerPicture() {
        return singerPicture;
    }

    public void setSingerPicture(String singerPicture) {
        this.singerPicture = singerPicture;
    }

    public String getSingerNationality() {
        return singerNationality;
    }

    public void setSingerNationality(String singerNationality) {
        this.singerNationality = singerNationality;
    }
}